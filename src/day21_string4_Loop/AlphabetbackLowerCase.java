package day21_string4_Loop;

public class AlphabetbackLowerCase {
    public static void main(String[] args) {
        /*
        write a program that will print all the alphabet letters backwards in
lowercase from 'z' to 'a'
         */

       //z -a lowercase

        char b ='z';
        while (b >= 'a'){
            System.out.print(b+ " ");
            b--;

        }
    }
}
