package day08_relational_operators;

public class RelationalOperatorExamples {

    public static void main(String[] args) {

        System.out.println(10 > 5); // true
        System.out.println(10 < 5 ); // false;

        int a = 5 ;
        int b = 3;

        System.out.println( a > b);  // 5>3; // true
        boolean aLessThan = a < b;  // true
        System.out.println( aLessThan);

        System.out.println(6>=6); // true
        System.out.println(6<=6);  // true

        System.out.println(4==4); // true
        System.out.println(4!=4); // false

        System.out.println(3!=1);  // true
        System.out.println();


    }
}
