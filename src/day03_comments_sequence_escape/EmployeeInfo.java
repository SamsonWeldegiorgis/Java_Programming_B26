package day03_comments_sequence_escape;

public class EmployeeInfo {

    public static void main(String[] args) {


        System.out.println(" Company Name :  Google ");
        System.out.println(" Employee :      Khalid");
        System.out.println(" ID :            12345");
        System.out.println(" Job Title :     SDET" );


        System.out.println();


        System.out.println(" Company Name : \t Google ");
        System.out.println(" Employee :  \t\t Khalid");
        System.out.println(" ID :    \t\t\t 12345");
        System.out.println(" Job Title : \t     SDET" );
    }
}
